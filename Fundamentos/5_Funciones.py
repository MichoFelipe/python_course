# Funcion para obtener el máximo valor.
max_valor = max(10,15,30,12,8,19)
print("max_valor: ", max_valor)

# Funcion para obtener el mínimo valor.
min_valor = min(10,15,30,12,8,19)
print("min_valor: ", min_valor)

# Funcion para reemplazar una palabra
mensaje = "Python es muy facil"
print(mensaje.replace('facil', 'xD'))

# Funcion para cambiar el texto a mayúsculas
nombre = "aureliano"
print(nombre.upper())

# Funcion para mostrar la lista invertida
posiciones = [1,2,3]
posiciones.reverse()
print(posiciones)